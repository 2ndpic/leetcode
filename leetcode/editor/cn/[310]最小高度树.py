class Solution:
    def findMinHeightTrees(self, n: int, edges: List[List[int]]) -> List[int]:
        """
        BFS å‰¥æ´‹è‘±
        - å»ºç«‹å‡ºåº¦é‚»æ¥è¡¨
        - å»ºç«‹åº¦è¡¨ï¼ˆæ— å‘å›¾ä¸­å‡ºåº¦=å…¥åº¦ï¼‰
        - å¯¹åº¦ä¸º1çš„èŠ‚ç‚¹ï¼ˆå¶å­èŠ‚ç‚¹ï¼‰è¿›è¡Œå…¥é˜Ÿ
        """
        if n == 1:return [0]
        g = [[] for _ in range(n)]
        degrees = [0] * n
        for u, v in edges:
            g[u].append(v)
            g[v].append(u)
            degrees[u] += 1
            degrees[v] += 1
        q = deque()
        for i in range(n):
            if degrees[i] == 1:
                q.append(i)

        while q:
            res = []
            for i in range(len(q)):
                u = q.popleft()
                res.append(u)
                for v in g[u]:
                    degrees[v] -= 1
                    if degrees[v] == 1:
                        q.append(v)
        return res
