import collections
# leetcode submit region begin(Prohibit modification and deletion)
class Solution:
    def canConvertString(self, s: str, t: str, k: int) -> bool:
        if len(s) != len(t):
            return False
        cnt = collections.defaultdict(lambda : k//26)
        for i in range(1, k % 26 + 1):
            cnt[i] += 1
        for i, j in zip(s, t):
            if i != j:
                tmp = (ord(j) - ord(i) + 26) % 26
                cnt[tmp] -= 1
                if cnt[tmp] < 0:
                    return False
        return True

