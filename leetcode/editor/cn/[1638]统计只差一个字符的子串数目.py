class Solution:
    def countSubstrings(self, s: str, t: str) -> int:
        m, n, ans = len(s), len(t), 0
        for i in range(m):
            for j in range(n):
                diff, k = 0, 0
                while i + k < m and j + k < n:
                    diff += (s[i + k] != t[j + k])
                    if diff == 1:
                        ans += 1
                    elif diff > 1:
                        break
                    k += 1
        return ans


