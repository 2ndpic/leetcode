class Solution:
    def minimumOperations(self, nums: List[int]) -> int:
        def get(start):
            """
            :param start: 0è¡¨ç¤ºå¶æ•°ä¸‹æ ‡ï¼Œ1è¡¨ç¤ºå¥‡æ•°ä¸‹æ ‡
            :return: æœ€å¤§æ¬¡æ•°å¯¹åº”çš„å€¼, æœ€å¤§æ¬¡ï¼Œæ¬¡å¤§æ¬¡æ•°å¯¹åº”çš„å€¼, æ¬¡å¤§æ¬¡æ•°
            """
            freq = Counter(nums[start::2])
            best = freq.most_common(2)
            if not best:
                return 0, 0, 0, 0
            if len(best) == 1:
                return *best[0], 0, 0
            return *best[0], *best[1]

        n = len(nums)
        e1k, e1v, e2k, e2v = get(0)
        o1k, o1v, o2k, o2v = get(1)
        if e1k != o1k: return n - e1v - o1v
        return n - max(e1v + o2v, e2v + o1v)
