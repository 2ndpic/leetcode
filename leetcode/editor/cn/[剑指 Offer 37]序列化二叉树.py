import collections

class Codec:

    def serialize(self, root):
        """Encodes a tree to a single string.
        
        :type root: TreeNode
        :rtype: str
        """
        arr = []
        q = collections.deque([root])
        while q:
            node = q.popleft()
            arr.append(node.val if node else None)
            if node is not None:
                q.append(node.left)
                q.append(node.right)
        return str(arr)

